export const contactId = '0x721BaCBD1Dc677209F32938Aef1B96C294444e04';

export async function initializeWeb3() {
  if (!window.ethereum) {
    // Swal.fire({
    //   position: 'top-end',
    //   icon: 'error',
    //   title: 'Please Install MetaMask First',
    //   showConfirmButton: false,
    //   timer: 2000,
    // }).then(() => {
    //   window.location.href = '/';
    // });
    return alert("Please Install MetaMask First");
  }

  try {
    const web3Instance = new Web3(window.ethereum);
    const accounts = await window.ethereum.request({
      method: 'eth_requestAccounts',
    });
    const account = accounts[0];
    const contractInstance = new web3Instance.eth.Contract(
      CashLessContract,
      contactId
    );
    return { web3Instance, account, contractInstance };
  } catch (error) {
    return `Error initializing Web3: ${error.message}`;
  }
}
